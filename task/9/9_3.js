// if, else if, else
// for, while loops
// ✔ Example: Print even numbers from 1 to 20


for (let i=2;i<=20;i++){
    if (i%2==0)
        console.log(i)
}


for(let k=2;k<=20;k+=2){
    console.log(k)
}

j=1
while(j<=20){
    if (j%2==0)
        console.log(j)
    j++
}